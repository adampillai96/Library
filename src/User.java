public class User {

    String password;
    String id;
    String roll;


    public User(){}

    public void setId(String id) {
        this.id = id;
    }

    public void setPassword(String password) {
        this.password = pass;
    }


    public void setRoll(String roll) {
        this.roll = roll;
    }

    public String getId() {
        return id;
    }

    public String getPassword() {
        return password;
    }

    public String getRoll() {
        return roll;
    }



}
